class Solution:
    
    def reverse(self, head):
        prev = None
        current = head
        while current:
            next_node = current.next
            current.next = prev
            prev = current
            current = next_node
        return prev
    
    def addOne(self, head):
        # Step 1: Reverse the linked list
        head = self.reverse(head)
        
        # Step 2: Add one to the reversed list
        current = head
        carry = 1
        
        while current:
            new_value = current.data + carry
            carry = new_value // 10
            current.data = new_value % 10
            
            # Move to the next node, or handle carry at the end
            if not current.next and carry > 0:
                current.next = Node(carry)
                carry = 0
            current = current.next
        
        # Step 3: Reverse the list again to restore original order
        return self.reverse(head)
    

