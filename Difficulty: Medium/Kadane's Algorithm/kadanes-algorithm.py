class Solution:
    ##Complete this function
    #Function to find the sum of contiguous subarray with maximum sum.
    def maxSubArraySum(self,arr):
        ##Your code here
        Maxx = float("-inf")
        Sum = 0
        for num in arr:
            Sum += num
            if Sum > Maxx:
                Maxx = max(Maxx,Sum)
            if Sum <= 0:
                Sum = 0
        return Maxx


